      <h2 class="architect-intro">
       <span>renowned architect</span>
       <span class="architect-name">frida escobedo</span> brings a refreshingly unique cultural addition to <span class="ap-location">new york city</span>
      </h2>

      <p>The quaint residential block of Bergen Street welcomes Taller Frida Escobedo's first ever condominium residence. Driven by neighborhood context, the facade takes inspiration from the quintessential bay-window brownstones adorning the neighborhood, interpreting the geometry and materiality in a contemporary and intentional manner. Comprised of custom hand-made brick modules, a humble yet elegant form, the facade promotes the philosophy of complexity in simplicity.
  Staggering bricks to bays, the architecture's rhythmic shape transitions pace from activated street to domestic refuge.</p>

      <p>The crafted, natural masonry folds, stacks, and interlocks, to create the porous brick lattice structures seen throughout Bergen; a variety of dynamic conditions that allow for light, shadow, and air to seep through its design. A balance of connection between the in- and outdoors.</p>
